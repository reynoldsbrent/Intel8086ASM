; BCD TO 16 BIT ASCII NUMBER

.DATA
BCD_DATA DB 45H
ASCII_RESULT DW ?


.CODE
START:
    MOV AX, DATA
    MOV DS, AX
    XOR AX, AX
    XOR DX, DX
    
    MOV AL, BCD_DATA
    
    MOV AH, BCD_DATA ;AX CONTAINS 4545
    AND AL, 0FH
    AND AH, 0F0H
    MOV CL, 4
    SHR AH, CL
    OR AX, 3030H
    MOV DX, AX ;STORING RESULT
    MOV DX, ASCII_RESULT
    
    
    
    INT 21H

END START